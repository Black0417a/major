function g(category, majorClass, names) {
  return { category, majorClass, names };
}

function clamp(n) {
  return Math.max(0, Math.min(1, Number(n.toFixed(2))));
}

function vectorFor(category, majorClass, name) {
  const base = {
    math: .34, physics: .24, chemistry: .22, biology: .22, humanities: .28, language: .24,
    research: .38, realistic: .34, artistic: .25, social: .34, enterprising: .32, conventional: .34,
    logic: .4, data: .34, communication: .34, handsOn: .32, spatial: .25, creativity: .28,
    openness: .4, responsibility: .48, patience: .42, pressure: .38, leadership: .26,
    stability: .38, income: .36, socialValue: .38
  };
  const text = `${majorClass}${name}`;
  for (const [pattern, profile] of classProfiles) {
    if (pattern.test(text)) Object.assign(base, profile);
  }
  if (/人工智能|数据|软件|网络|金融科技|区块链|密码|智能/.test(text)) {
    base.data = Math.max(base.data, .86);
    base.logic = Math.max(base.logic, .86);
    base.income = Math.max(base.income, .78);
  }
  if (/师范|教育|护理|临床|口腔|预防|社会工作/.test(text)) {
    base.social = Math.max(base.social, .82);
    base.socialValue = Math.max(base.socialValue, .86);
  }
  return Object.fromEntries(Object.entries(base).map(([k, v]) => [k, clamp(v)]));
}

